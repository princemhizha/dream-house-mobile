const toPropertyTypeValue = (propertyType: RawProperty['propertyType']): Property['type'] =>
  propertyType === 'studentRoom' ? 'student_room' : propertyType;

const toPropertyCategory = (property: RawProperty): Property['category'] => {
  if (property.listingType === 'buy') {
    return 'sale';
  }

  if (property.listingType === 'shortStay') {
    return 'short_stay';
  }

  if (property.propertyType === 'studentRoom') {
    return 'student';
  }

  return 'rent';
};

const toPriceUnit = (property: RawProperty): Property['priceUnit'] => {
  if (property.listingType === 'shortStay') {
    return 'night';
  }

  return 'month';
};

export const mockProperties: Property[] = rawProperties.map((property) => ({
  ...property,
  price: Math.min(Math.max(property.price, 150), 2500),
  priceUnit: toPriceUnit(property),
  type: toPropertyTypeValue(property.propertyType),
  category: toPropertyCategory(property),
  parking: property.parkingSpaces > 0,
  verified: property.landlord.verified,
}));

export const getFeaturedProperties = (): Property[] =>
  mockProperties.filter((p) => p.featured);

export const getNewlyAdded = (): Property[] =>
  [...mockProperties]
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())
    .slice(0, 8);

export const getTrending = (): Property[] =>
  [...mockProperties].sort((a, b) => b.views - a.views).slice(0, 6);

export const getStudentProperties = (): Property[] =>
  mockProperties.filter((p) => p.propertyType === 'studentRoom');

export const getPropertyById = (id: string): Property | undefined =>
  mockProperties.find((p) => p.id === id);
